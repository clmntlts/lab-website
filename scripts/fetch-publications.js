// Fetches publication metadata from Crossref for each DOI listed in
// src/data/publications.js and writes YAML files into src/content/publications/.
// Existing hand-crafted YAML files are left untouched; only DOI-sourced files
// (identified by the `# doi-sourced: true` header comment) are overwritten.

import { writeFileSync, readFileSync, existsSync, readdirSync } from 'fs'
import { resolve, dirname } from 'path'
import { fileURLToPath } from 'url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const CONTENT_DIR = resolve(__dirname, '../src/content/publications')

const { default: publications } = await import('../src/data/publications.js')

if (publications.length === 0) {
  console.log('No publications in src/data/publications.js — nothing to fetch.')
  process.exit(0)
}

// ─── Crossref helpers ────────────────────────────────────────────────────────

async function fetchCrossref(doi) {
  const url = `https://api.crossref.org/works/${encodeURIComponent(doi)}`
  const res = await fetch(url, {
    headers: { 'User-Agent': 'DeliLabWebsite/1.0 (mailto:clement.letesson@gmail.com)' },
  })
  if (!res.ok) throw new Error(`Crossref ${res.status} for ${doi}`)
  return (await res.json()).message
}

function extractAuthors(list = []) {
  return list.map((a) => (a.name ? a.name : [a.family, a.given].filter(Boolean).join(', ')))
}

function extractYear(work) {
  const parts =
    work['published-print']?.['date-parts']?.[0] ??
    work['published-online']?.['date-parts']?.[0] ??
    work['created']?.['date-parts']?.[0]
  return parts?.[0] ?? null
}

function mapType(crossrefType) {
  const map = {
    'journal-article':     'journal',
    'proceedings-article': 'conference',
    'posted-content':      'preprint',
    'book-chapter':        'book-chapter',
    'dissertation':        'thesis',
    'workshop':            'workshop',
  }
  return map[crossrefType] ?? 'journal'
}

// ─── YAML serialiser (no external dependency) ────────────────────────────────

function yamlStr(s) {
  if (s == null) return 'null'
  // Use block scalar for long strings, quoted for short ones with special chars
  if (s.includes('\n') || s.length > 80) {
    const indented = s.replace(/\n/g, '\n  ')
    return `>-\n  ${indented}`
  }
  if (/[:#\[\]{}&*!,|>'"%@`]/.test(s) || s.startsWith('-') || s.trim() !== s) {
    return `"${s.replace(/\\/g, '\\\\').replace(/"/g, '\\"')}"`
  }
  return s
}

function toYaml(data) {
  const lines = ['# doi-sourced: true  — generated by scripts/fetch-publications.js']
  for (const [k, v] of Object.entries(data)) {
    if (v == null) continue
    if (Array.isArray(v)) {
      if (v.length === 0) { lines.push(`${k}: []`); continue }
      lines.push(`${k}:`)
      v.forEach((item) => lines.push(`  - ${yamlStr(item)}`))
    } else if (typeof v === 'boolean') {
      lines.push(`${k}: ${v}`)
    } else if (typeof v === 'number') {
      lines.push(`${k}: ${v}`)
    } else {
      lines.push(`${k}: ${yamlStr(String(v))}`)
    }
  }
  return lines.join('\n') + '\n'
}

// ─── Slug from DOI ───────────────────────────────────────────────────────────

function slugFromDoi(doi) {
  return doi.replace(/[^a-z0-9]/gi, '-').replace(/-+/g, '-').replace(/^-|-$/g, '').toLowerCase()
}

// ─── Main ────────────────────────────────────────────────────────────────────

console.log(`Fetching metadata for ${publications.length} publication(s)…`)

for (const pub of publications) {
  const slug = slugFromDoi(pub.doi)
  const outPath = resolve(CONTENT_DIR, `${slug}.yaml`)

  // Skip hand-crafted files (no doi-sourced header)
  if (existsSync(outPath)) {
    const existing = readFileSync(outPath, 'utf-8')
    if (!existing.startsWith('# doi-sourced:')) {
      console.log(`  ${pub.doi} — skipped (hand-crafted file exists)`)
      continue
    }
  }

  process.stdout.write(`  ${pub.doi} … `)
  try {
    const work = await fetchCrossref(pub.doi)

    const year = extractYear(work)
    const venue = work['container-title']?.[0] ?? work['publisher'] ?? ''
    const venueShort = work['short-container-title']?.[0] ?? null

    const record = {
      title:      work.title?.[0] ?? pub.doi,
      authors:    extractAuthors(work.author),
      year:       year ?? new Date().getFullYear(),
      venue,
      ...(venueShort ? { venueShort } : {}),
      type:       mapType(work.type),
      doi:        pub.doi,
      ...(pub.pdf ? { pdf: pub.pdf } : {}),
      abstract:   work.abstract?.replace(/<[^>]+>/g, '') ?? null,
      keywords:   (work.subject ?? []).slice(0, 8),
      categories: [],
      featured:   false,
    }

    writeFileSync(outPath, toYaml(record), 'utf-8')
    console.log('OK')
  } catch (err) {
    console.error(`FAILED — ${err.message}`)
  }
}

console.log('Done.')
